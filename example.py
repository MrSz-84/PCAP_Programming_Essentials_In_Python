import time # LINIA I

# Napisz pętlę for, która liczy do pięciu.
for count in range (1,6):
    # Ciało pętli: wyświetl w oknie konsoli numer iteracji i słowo "Mississippi".
    print(count, "Mississippi")
    time.sleep(1) # LINIA II

